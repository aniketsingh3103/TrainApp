    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseClicked
        int amount=0;
        try{

            Registry reg=LocateRegistry.getRegistry("127.0.0.1",1111);
            LoginControl l=(LoginControl)reg.lookup("login");
            amount=l.getTrainInfo(jComboBox1.getSelectedItem().toString(),jComboBox2.getSelectedItem().toString());

            if(amount==-1){
                JOptionPane.showMessageDialog(null,"WE DONT HAVE SERVICE TO THIS OPTION");
            }else{

                JOptionPane.showMessageDialog(null,"TRAIN FOUND TOTAL AMOUNT IS:"+amount);
                this.dispose();
                AddPassenger pass=new AddPassenger(amount);
                pass.setVisible(true);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        
        
        
    }//GEN-LAST:event_jPanel3MouseClicked

